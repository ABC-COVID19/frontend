	<h2 class="fontCblue font-weight-bold mt-5">Projeto ICAM-COVID19 </h2>				
		<div class="textBoxmain text-justify">

<p><span>O projeto ICAM-COVID19 nasce da colabora&ccedil;&atilde;o volunt&aacute;ria e desinteressada entre estudantes de Medicina, internos de forma&ccedil;&atilde;o geral e m&eacute;dicos especialistas do Centro Acad&eacute;mico Hospitalar do Algarve e do <a href="https://abcmedicalg.pt/" target="_blank">ABC- Algarve Biomedical Center</a> , com investigadores do <a href="http://bioisi.pt/" target="_blank">BioISI</a> - <a href="https://ciencias.ulisboa.pt/" target="_Blank">Faculdade de Ci&ecirc;ncias da Universidade de Lisboa</a>, em parceria com as empresas <a href="https://www.algardata.com/" target="_blank">Algardata</a>, <a href="http://www.sensocomum.pt" target="_blank">Senso Comum</a>, e a task force volunt&aacute;ria <a href="https://tech4covid19.org/" target="_blank">Tech4Covid</a>.</span></p>
<p><span>O que motivou esta colabora&ccedil;&atilde;o foi a necessidade sentida pelos m&eacute;dicos do Algarve de se manterem informados de forma cientificamente rigorosa, atual e constante sobre o agente patog&eacute;nico SARS-CoV-2 e a doen&ccedil;a por si provocada, COVID-19.</span></p>
<p><span>O projeto ICAM assenta num algoritmo desenvolvido pela equipa de volunt&aacute;rios que, de forma automatizada, atualiza diariamente uma base de dados com todas as novas publica&ccedil;&otilde;es cient&iacute;ficas indexadas nos tr&ecirc;s principais reposit&oacute;rios para a &aacute;rea biom&eacute;dica: PubMed, BioRxiv e MedRxiv.&nbsp;</span></p>
<p><span>Uma equipa de revisores volunt&aacute;rios composta por m&eacute;dicos, investigadores e estudantes de Medicina efetua a revis&atilde;o sistem&aacute;tica das novas publica&ccedil;&otilde;es, identificando os artigos mais relevantes e criando uma sinopse em portugu&ecirc;s para publica&ccedil;&atilde;o direta no website associado, ficando toda a informa&ccedil;&atilde;o agregada por &aacute;rea de relev&acirc;ncia cl&iacute;nica, como forma de facilitar a sua consulta. A plataforma criada permite ainda a subscri&ccedil;&atilde;o para alertas sobre atualiza&ccedil;&otilde;es dentro das &aacute;reas espec&iacute;ficas de interesse e o um link de acesso aos artigos originais revistos.</span></p>
<p><span>A partilha de conhecimento cient&iacute;fico &eacute; fundamental para o bom desempenho dos profissionais de sa&uacute;de no combate &agrave; pandemia, considerando as zonas de sombra que ainda subsistem relativamente ao conhecimento sobre o novo coronav&iacute;rus, &agrave; forma de conter a sua propaga&ccedil;&atilde;o e aos meios terap&ecirc;uticos mais eficazes para debelar a COVID-19.</span></p>
<p><span>Esta plataforma aberta permite um acesso r&aacute;pido &agrave; informa&ccedil;&atilde;o cient&iacute;fica relevante e atualizada sobre a COVID-19 pelos profissionais de sa&uacute;de, para os apoiar na tomada de decis&otilde;es cl&iacute;nicas adequadas. Esperamos que seja &uacute;til para todos!</span><span><br /></span><span><br /></span><span><br /></span><span>Em nome da equipa ICAM CoVID-19</span></p>
<p><strong>Alexandre Teixeira</strong></p>
<p><span>(Coordenador do projeto ICAM)</span></p>
